    private void tblKMMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKMMouseClicked
        // TODO add your handling code here:
        int row = this.tblKM.getSelectedRow();

        if (row == -1) {
            return;
        }

        String idStr = this.tblKM.getValueAt(row, 0).toString();
        String Ma = this.tblKM.getValueAt(row, 1).toString();
        String ten = this.tblKM.getValueAt(row, 2).toString();
        String Sdt = this.tblKM.getValueAt(row, 3).toString();

        this.lblId.setText(idStr);
        this.txtMa.setText(Ma);
        this.txtTen.setText(ten);
        this.txtPhanTram.setText(Sdt);

    }//GEN-LAST:event_tblKMMouseClicked

    private KhuyenMai getFormData() {
        String id = lblId.getText().trim();
        String ma = txtMa.getText().trim();
        String ten = txtTen.getText().trim();
        String phanTram = txtPhanTram.getText().trim();
        if (ma.length() == 0
                || ten.length() == 0
                || phanTram.length() == 0) {
            JOptionPane.showMessageDialog(this, "Không để trống");
            return null;
        }
        KhuyenMai km = new KhuyenMai(id, ma, ten, phanTram);
        return km;
    }

