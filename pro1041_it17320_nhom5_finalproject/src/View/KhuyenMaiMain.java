    private void loadTable() {
        DefaultTableModel dtm = (DefaultTableModel) this.tblKM.getModel();

        dtm.setRowCount(0);

        for (KhuyenMai km : this.khuyenMaiI.getListKM()) {
            Object[] rowData = {
                km.getId(),
                km.getMa(),
                km.getTen(),
                km.getPhanTramKM(),};

            dtm.addRow(rowData);
        }
    }

